void bmp_drawstring(bitmap_t *bmp, int x, int y, int halign, int valign, int ang, const char *str, int rgb)
{
  int i,len,w,tx,ty,pos,j,xo,yo;
  vector_t p0,p1,v0,v1;
  matrix_t tr,m;
  int *vec;

  w = 0;
  yo = xo = 0;
  len = strlen(str);

  /* Calculate total width of string */
  for (i=0; i<len; i++) {
    if (str[i] < ' ' || str[i] > 126)
      return;
    w += simplex[str[i]-32][1];
  }

  /* Calculate horiz/vert alignment */
  tx = ty = 0;
  if (halign == CENTER)
    tx = -w/2;
  else if (halign == RIGHT)
    tx = -w;
  if (valign == TOP)
    ty = -22;

  /* Precalc origin Translate+Rotate matrix */
  tr = matxmat(T(x,y),R(ang));
  tr = matxmat(tr,S(0.5,0.5));
  for (i=0; i<len; i++) {
    vec = simplex[str[i]-32];

    v1.v[0] = -1;
    v1.v[1] = -1;
    v1.v[2] = 1;

    /* Calculate per-character offset Translate matrix */
    m = matxmat(tr,T(tx+xo,ty+yo));
    xo += vec[1];
    //yo -= 24;

    pos = 2;   
    for (j=0; j<vec[0]; j++) {
      v0 = v1;
      v1.v[0] = vec[pos++];
      v1.v[1] = vec[pos++];
      v1.v[2] = 1;
      if (v0.v[0] != -1 && v1.v[0] != -1) {
        p0 = matxvec(m,v0);
        p1 = matxvec(m,v1);
        bmp_line(bmp, p0.v[0], p0.v[1], p1.v[0], p1.v[1], rgb);
      }
    }
  } 
}

