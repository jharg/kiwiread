static void *zmalloc(size_t sz)
{
  void *ptr;

  if ((ptr = malloc(sz)) != NULL)
    memset(ptr, 0, sz);
  return ptr;
}

bitmap_t *bmp_alloc(int w, int h)
{
  bitmap_t *bmp;

  w = (w + 3) & ~3;
  bmp = zmalloc(sizeof(*bmp));
  bmp->data = zmalloc(w*h*sizeof(int));
  bmp->w = w;
  bmp->h = h;
  return bmp;
}

void bmp_putpixel(bitmap_t *bmp, int x, int y, int rgb)
{
  if (x >= bmp->w || y >= bmp->h || x < 0 || y < 0)
    return;
  bmp->data[x+y*bmp->w] = rgb;
}

static int swap(int *a, int *b)
{
  int tmp = *a;
  *a = *b;
  *b = tmp;
}

void bmp_hline(bitmap_t *bmp, int x0, int x1, int y0, int rgb)
{
  if (x0 > x1) {
    swap(&x0,&x1);
  }
  while (x0 <= x1)
    bmp_putpixel(bmp, x0++, y0, rgb);
}

void bmp_vline(bitmap_t *bmp, int x0, int y0, int y1, int rgb)
{
  if (y0 > y1) {
    swap(&y0,&y1);
  }
  while (y0 <= y1)
    bmp_putpixel(bmp, x0, y0++, rgb);
}

void bmp_rect(bitmap_t *bmp, int x0, int y0, int x1, int y1, int rgb)
{
  bmp_hline(bmp, x0, x1, y0, rgb);
  bmp_hline(bmp, x0, x1, y1, rgb);
  bmp_vline(bmp, x0, y0, y1, rgb);
  bmp_vline(bmp, x1, y0, y1, rgb);
}

void bmp_line(bitmap_t *bmp, int x0, int y0, int x1, int y1, int rgb)
{
  int dx, dy, sx, sy, e, e2;

  if (x0 == x1)
    bmp_vline(bmp, x0, y0, y1, rgb);
  else if (y0 == y1)
    bmp_hline(bmp, x0, x1, y0, rgb);
  else {
    /* Calculate delta xy and errorterm */
    sx = 1;
    sy = 1;
    if ((dx = x1 - x0) < 0) {
      sx = -sx;
      dx = -dx;
    }
    if ((dy = y1 - y0) < 0) {
      sy = -sy;
      dy = -dy;
    }
    e = dx - dy;

    for(;;) {
      bmp_putpixel(bmp, x0, y0, rgb);
      if (x0 == x1 && y0 == y1)
	break;
      e2 = 2*e;
      if (e2 > -dy) {
	e  -= dy;
	x0 += sx;
      }
      if (x0 == x1 && y0 == y1) {
	bmp_putpixel(bmp, x0, y0, rgb);
	break;
      }
      if (e2 < dx) {
	e  += dx;
	y0 += sy;
      }
    }
  }
}

/* Draw polygon */
void bmp_poly(bitmap_t *bmp, int nvertex, int *xy, int rgb)
{
  int i;
  
  for (i=0; i<nvertex-1; i++) {
    bmp_line(bmp, xy[i*2], xy[i*2+1], xy[i*2+2], xy[i*2+3], rgb);
  }
  bmp_line(bmp, xy[i*2], xy[i*2+1], xy[0], xy[1], rgb);
}

void bmp_polyline(bitmap_t *bmp, int nvertex, int *xy, int rgb)
{
  int i;
  
  for (i=0; i<nvertex-1; i++) {
    bmp_line(bmp, xy[i*2], xy[i*2+1], xy[i*2+2], xy[i*2+3], rgb);
  }
}

struct edge_t
{
  double x0,y0,y1,m;
  int flag;
};

int sgn(double val)
{
  return (0 < val) - (val < 0);
}

int cmp(const void *a, const void *b)
{
  const struct edge_t *ea = a;
  const struct edge_t *eb = b;
  int rc;

  /* Sort by sign, y0, x0 */
  if ((rc = sgn(ea->flag - eb->flag)) == 0) {
    if ((rc = sgn(ea->y0 - eb->y0)) == 0)
      rc = sgn(ea->x0 - eb->x0);
  }
  return rc;
}

void bmp_polyfill(bitmap_t *bmp, int nvertex, int *xy, int rgb)
{
  int x0,y0,x1,y1,c,n,e2,done;
  struct edge_t *global;

  c = 0;
  global = alloca(sizeof(struct edge_t)*nvertex);
  for (n=0; n<nvertex; n++) {
    x0 = xy[n*2];
    y0 = xy[n*2+1];
    x1 = xy[(n*2+2) % (nvertex*2)];
    y1 = xy[(n*2+3) % (nvertex*2)];
    if (y0 > y1) {
      swap(&y0,&y1);
      swap(&x0,&x1);
    }
    /* dy is always positive */
    if (y0 != y1) {
      global[c].x0 = x0;
      global[c].y0 = y0;
      global[c].y1 = y1;
      global[c].flag = 0;
      global[c].m  = (double)(x1-x0)/(y1-y0);
      c++;
    }
  }
  for(;;) {
    /* Sort verticies by increasing y/x */
    qsort(global, c, sizeof(struct edge_t), cmp);

    /* If no verticies remain, done */
    if (global[0].flag)
      break;

    /* y0 is lowest Y coordinate */
    y0 = global[0].y0;

    /* loop through remaining valid verticies.
     * Draw horizontal lines while Y coordinates are the same
     */
    for (n=0; n<c && global[n].y0 == y0 && global[n].flag == 0; n+=2) {
      /* ASSERT(global[n].y0 == global[n+1].y0) */

      /* Draw lines while y0 are same */
      bmp_hline(bmp, global[n].x0, global[n+1].x0, y0, rgb); 
    }

    /* Increment X/Y */
    for (n=0; n<c && global[n].y0 == y0; n++) {
      /* Advance X by slope */
      global[n].x0 += global[n].m;

      /* Increment Y - if above MaxY, flag as complete */
      if (++global[n].y0 >= global[n].y1) {
 	global[n].flag = 1;
      }
    }
  }
}

