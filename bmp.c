static void _put32(uint8_t *p, int v)
{
  p[0] = v;
  p[1] = v >> 8;
  p[2] = v >> 16;
  p[3] = v >> 24;
}

static void _put24(uint8_t *p, int v)
{
  p[0] = v;
  p[1] = v >> 8;
  p[2] = v >> 16;
}

static void _put16(uint8_t *p, int v)
{
  p[0] = v;
  p[1] = v>>8;
}

#define HDRLEN 54
#define NBYTES 4
void bmp_write(bitmap_t *bmp, const char *file)
{
  FILE *fp;
  int len, i, j;
  uint8_t h[HDRLEN] = { 0 };
  uint8_t rgb[4];

  if ((fp = fopen(file, "w")) != NULL) {
    len = HDRLEN + NBYTES*bmp->w*bmp->h;

    h[0] = 'B';
    h[1] = 'M';
    _put32(h+0x02, len);
    _put32(h+0x0A, HDRLEN);          // offset of bmp data

    _put32(h+0x0e, 40);              // size of DIB header
    _put32(h+0x12, bmp->w);          // width
    _put32(h+0x16, bmp->h);          // height
    _put16(h+0x1a, 1);               // planes
    _put16(h+0x1c, 8 * NBYTES);      // bits per pixel
    _put32(h+0x22, 16);              // image size
    _put32(h+0x26, 2835);            // horiz resolution (pixel per meter)
    _put32(h+0x2a, 2835);            // vert resolution (pixel per meter)
    fwrite(h, HDRLEN, 1, fp);

#if (NBYTES == 4)
    fwrite(bmp->data, 4, bmp->w*bmp->h, fp);
#else
    for (j=0; j<bmp->h; j++) {
      for (i=0; i<bmp->w; i++) {
	_put24(rgb, bmp->data[i + j*bmp->w]);
	fwrite(rgb, 3, 1, fp);
      }
    }
#endif
    fclose(fp);
  }
}

/* 0      0       tx
 * 0      0       ty
 * 0      0       1
 *
 * sx     0       0
 * 0      sy      0
 * 0	  0       1
 *
 * cos(a) -sin(a) 0
 * sin(a) cos(a)  0
 * 0      0       1
 */
vector_t vecinit(double x, double y)
{
  vector_t r;

  r.v[0] = x;
  r.v[1] = y;
  r.v[2] = 1;
  return r;
}

/* Matrix x Vector */
vector_t matxvec(matrix_t m, vector_t v)
{
  vector_t r;
  int i,j;

  memset(&r, 0, sizeof(r));
  for (i=2;i>=0;i--) {
    for (j=2;j>=0;j--) {
      r.v[i] += m.m[i][j]*v.v[j];
    }
    /* normalize */
    r.v[i] /= r.v[2];
  }
  return r;
}

/* Matrix x Matrix */
matrix_t matxmat(matrix_t a, matrix_t b)
{
  int i, j, k;
  matrix_t r;

  memset(&r, 0, sizeof(r));
  for (i=2;i>=0;i--) {
    for (j=2;j>=0;j--) {
      for (k=2;k>=0;k--) {
	r.m[i][j] += a.m[i][k]*b.m[k][j];
      }
      /* normalize */
      r.m[i][j] /= r.m[2][2];
    }
  }
  return r;
}

/* Identity matrix */
matrix_t I()
{
  matrix_t r;

  memset(&r, 0, sizeof(r));
  r.m[0][0] = 1;
  r.m[1][1] = 1;
  r.m[2][2] = 1;
  return r;
}

/* Translate matrix */
matrix_t T(double tx, double ty)
{
  matrix_t r;

  memset(&r, 0, sizeof(r));
  r.m[0][0] = 1;
  r.m[0][2] = tx;
  r.m[1][1] = 1;
  r.m[1][2] = ty;
  r.m[2][2] = 1;
  return r;
}

/* Scale matrix */
matrix_t S(double sx, double sy)
{
  matrix_t r;

  memset(&r, 0, sizeof(r));
  r.m[0][0] = sx;
  r.m[1][1] = sy;
  r.m[2][2] = 1;
  return r;
}

/* Rotate matrix */
matrix_t R(double angle)
{
  matrix_t r;
  double rad = (angle * M_PI) / 180.0;
  
  memset(&r, 0, sizeof(r));
  r.m[0][0] = cos(rad);
  r.m[0][1] = -sin(rad);
  r.m[1][0] = sin(rad);
  r.m[1][1] = cos(rad);
  r.m[2][2] = 1;
  return r;
}

