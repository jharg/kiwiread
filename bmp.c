static void *zmalloc(size_t sz)
{
  void *ptr;

  if ((ptr = malloc(sz)) != NULL)
    memset(ptr, 0, sz);
  return ptr;
}

bitmap_t *bmp_alloc(int w, int h)
{
  bitmap_t *bmp;

  w = (w + 3) & ~3;
  bmp = zmalloc(sizeof(*bmp));
  bmp->data = zmalloc(w*h*sizeof(int));
  bmp->w = w;
  bmp->h = h;
  return bmp;
}

void bmp_putpixel(bitmap_t *bmp, int x, int y, int rgb)
{
  if (x >= bmp->w || y >= bmp->h || x < 0 || y < 0)
    return;
  bmp->data[x+y*bmp->w] = rgb;
}

void bmp_hline(bitmap_t *bmp, int x0, int x1, int y0, int rgb)
{
  int tmp;
  if (x0 > x1) {
    tmp = x0;
    x0 = x1;
    x1 = tmp;
  }
  while (x0 <= x1)
    bmp_putpixel(bmp, x0++, y0, rgb);
}

void bmp_vline(bitmap_t *bmp, int x0, int y0, int y1, int rgb)
{
  int tmp;

  if (y0 > y1) {
    tmp = y0;
    y0 = y1;
    y1 = tmp;
  }
  while (y0 <= y1)
    bmp_putpixel(bmp, x0, y0++, rgb);
}

void bmp_line(bitmap_t *bmp, int x0, int y0, int x1, int y1, int rgb)
{
  int dx, dy, sx, sy, e, e2;

  if (x0 == x1)
    bmp_vline(bmp, x0, y0, y1, rgb);
  else if (y0 == y1)
    bmp_hline(bmp, x0, x1, y0, rgb);
  else {
    sx = sy = 1;

    if ((dx = x1 - x0) < 0) {
      sx = -1;
      dx = -dx;
    }
    if ((dy = y1 - y0) < 0) {
      sy = -1;
      dy = -dy;
    }
    e = dx - dy;

    for(;;) {
      bmp_putpixel(bmp, x0, y0, rgb);
      if (x0 == x1 && y0 == y1)
	break;
      e2 = 2*e;
      if (e2 > -dy) {
	e  -= dy;
	x0 += sx;
      }
      if (x0 == x1 && y0 == y1) {
	bmp_putpixel(bmp, x0, y0, rgb);
	break;
      }
      if (e2 < dx) {
	e  += dx;
	y0 += sy;
      }
    }
  }
}

/* Draw polygon */
void bmp_poly(bitmap_t *bmp, int nvertex, int *xy, int rgb)
{
  int i, off;
  
  for (i=0; i<nvertex-1; i++) {
    bmp_line(bmp, xy[i*2], xy[i*2+1], xy[(i+1)*2], xy[(i+1)*2+1], rgb);
  }
  bmp_line(bmp, xy[i*2], xy[i*2+1], xy[0], xy[1], rgb);
}

